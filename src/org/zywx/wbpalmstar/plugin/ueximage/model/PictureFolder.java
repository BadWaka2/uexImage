package org.zywx.wbpalmstar.plugin.ueximage.model;

/**
 * Created by Fred on 2016/1/26.
 */
public class PictureFolder {
    private String firstImagePath;//
    private String folderName; //文件夹的名称
    private String folderPath; //文件夹路径
    private Integer count; //当前foler下图片的个数

    public String getFirstImagePath() {
        return firstImagePath;
    }

    public void setFirstImagePath(String firstImagePath) {
        this.firstImagePath = firstImagePath;
    }

    public String getFolderPath() {
        return folderPath;
    }

    public void setFolderPath(String folderPath) {
        this.folderPath = folderPath;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public String getFolderName() {
        return folderName;
    }

    public void setFolderName(String folderName) {
        this.folderName = folderName;
    }
}
